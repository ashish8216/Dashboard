     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'title' => 'required'

        ]);
        
        $setting = Banner::find($id);
        $setting->title = $request->get('title');
        $setting->sub_title = $request->get('sub_title');
        $setting->suber_title = $request->get('suber_title');
        $setting->link = $request->get('link');
        $setting->image = $request->get('image');
        $setting->save();
  
        return redirect('/admin/banners/1/edit')
        ->with('success','updated successfully');
    }

