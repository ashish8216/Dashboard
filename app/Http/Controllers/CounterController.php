     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $request->validate([
            'image' => 'required',
            'institutions' => 'required',
            'countries' => 'required',
            'students' => 'required',

        ]);
        
        $setting = Counter::find($id);
        $setting->image = $request->get('image');
        $setting->institutions = $request->get('institutions');
        $setting->countries = $request->get('countries');
        $setting->students = $request->get('students');
        $setting->save();
  
        return redirect('/admin/counters/1/edit')
        ->with('success','updated successfully');
    }

